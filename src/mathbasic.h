int add1(int num){
	return num + 1;
}

int sub1(int num){
	return num - 1;
}

int equalzero(int num){
	if (num == 1){
		return true;
	}
	else {
		return false;
	}
}

int equal0(int num){
	if (num == 0){
		return true;
	}
	else {
		return false;
	}
}



